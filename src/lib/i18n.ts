// ============ HELPERS ============
export type Dict = typeof translations.en

export function getDict(locale: Locale): Dict {
  return translations[locale] as Dict
}

// Helper to get a nested value by dot path
export function t(locale: Locale, path: string): string {
  const dict = translations[locale] as any
  const parts = path.split('.')
  let val = dict
  for (const p of parts) {
    if (val == null) return path
    val = val[p]
  }
  return typeof val === 'string' ? val : path
}

// Status translator - tries statusses.X first
export function translateStatus(locale: Locale, status: string): string {
  const dict = translations[locale] as any
  return dict.statuses?.[status] || status.replace(/_/g, ' ')
}

export function translateVehicle(locale: Locale, type: string): string {
  const dict = translations[locale] as any
  return dict.vehicles?.[type] || type
}

export function translatePosition(locale: Locale, pos: string): string {
  const dict = translations[locale] as any
  return dict.positions?.[pos] || pos
}

export function translateExpenseCategory(locale: Locale, cat: string): string {
  const dict = translations[locale] as any
  return dict.expenseCategories?.[cat] || cat
}
